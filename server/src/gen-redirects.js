// Generates deploy/redirects.caddy — the 301 redirect map for the cutover.
// Run with `npm run gen:redirects`.
//
// Two sources feed the map:
//   1. The 8 Rank Math redirects (hard-coded below, from discovery/redirects.md).
//   2. Blog post URLs: WordPress served posts at the site root (/<slug>/), the
//      new blog serves them under /blog/<slug>/ — every imported cs post needs
//      a redirect. These are generated from the articles table.
//
// Still missing: the 126 redirects from the WordPress "Redirection" plugin —
// export them (Tools -> Redirection -> Import/Export) into deploy/redirects-wp.caddy.

import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { db } from './db/index.js';

const here = dirname(fileURLToPath(import.meta.url));
const outPath = join(here, '../../deploy/redirects.caddy');

// From discovery/redirects.md — Rank Math redirect table.
const RANK_MATH = [
  ['/homeoffice', 'https://signi.com/'],
  ['/kompletni-cenik/', '/wp-content/uploads/2023/04/kompletni-cenik-signi-v-52023.pdf'],
  ['/webinar-signifikace', '/category/webinare/'],
  [
    '/wp-content/uploads/2022/07/budoucnost_hr_ebook_signi.pdf',
    '/wp-content/uploads/2022/08/budoucnost_hr_ebook_signi.pdf',
  ],
  ['/funkce', '/produkt/'],
  ['/aml', '/produkt/'],
  ['/bankid', '/bank-id/'],
  ['/jak-to-funguje', '/produkt/'],
];

const posts = db
  .prepare(
    "SELECT slug FROM articles WHERE language = 'cs' AND status = 'published' ORDER BY slug",
  )
  .all();

const lines = [
  '# signi.com 301 redirect map — generated by server/src/gen-redirects.js',
  '# Import this snippet into the signi.com site block in the Caddyfile.',
  '#',
  '# TODO: append the 126 redirects from the WordPress "Redirection" plugin',
  '#       (export via Tools -> Redirection -> Import/Export).',
  '',
  '# --- Rank Math redirects (discovery/redirects.md) ---',
  ...RANK_MATH.map(([from, to]) => `redir ${from} ${to} 301`),
  '',
  `# --- Blog posts: WordPress root URLs -> /blog/ (${posts.length} posts) ---`,
  ...posts.map((p) => `redir /${p.slug}/ /blog/${p.slug}/ 301`),
  '',
];

writeFileSync(outPath, lines.join('\n'));
console.log(`Wrote ${outPath} — ${RANK_MATH.length} Rank Math + ${posts.length} blog redirects.`);
